    32*bp*Tem;...
    4*mp*Tem;...
    32*kp*k+kp^2;...
    64*kp*bp+32*k*bp+32*kp^2*R*c+32*kp*R*Tem^2+32*kp*k*R*c+32*kp*b;...
    32*bp*R*Tem^2+32*bp*b+4*mp*k+16*mp*k+32*kp*m+64*kp*bp*R*c+32*kp*b*R*c+32*bp^2+32*bp*k*R*c;...
    4*mp*b+16*mp*bp+32*m*bp+4*mp*k*R*c+4*mp*R*Tem^2+32*m*kp*R*c+16*mp*kp*R*c+32*bp^2*R*c+32*bp*b*R*c;...
    4*mp*m+32*m*bp*R*c+4*mp*b*R*c+mp^2+16*mp*bp*R*c];
f = v/(4*mp*m*R*c+mp^2*R*c)-w;